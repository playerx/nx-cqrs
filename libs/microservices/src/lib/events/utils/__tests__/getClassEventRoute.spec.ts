import { UserUpdateEvent } from '../../__tests__/mockApi'
import { getClassEventRoute } from '../getClassEventRoute'

describe('get class event route', () => {
  it('follows the prototype chain', () => {
    const route = getClassEventRoute(UserUpdateEvent)
    expect(route).toMatchObject([
      'User',
      'BaseEvent',
      'UserEvent',
      'UserUpdateEvent',
    ])
  })
})
